function Skills({ mode }) {
  const [taps, setTaps] = useState([
    { id: 1, tap: "All", active: true },
    { id: 2, tap: "Technical", active: false },
    { id: 3, tap: "Frameworks & Libraries", active: false },
    { id: 4, tap: "Advanced Concepts", active: false },
    { id: 5, tap: "Tools", active: false },
  ]);

  const [skillsNow, setSkillsNow] = useState(skills);

  const handleTaps = (id) => {
    const newTaps = [...taps];

    newTaps.map((item) => {
      return item.id === id ? (item.active = true) : (item.active = false);
    });

    setTaps(newTaps);
  };

  const handleSkillsNow = (value) => {
    const newSkills = skills.filter(
      (skill) => skill.category === value || value === "All"
    );

    setSkillsNow(newSkills);
  };

  return (
    <div className={`my-skills ${mode}`} id="my-skills">
      <MainHeading title="My" subTitle="Skills" img="tools" />

      <ul className="taps">
        {taps.map((item) => {
          return (
            <li
              key={item.id}
              className={`same-text ${item.active ? "active" : ""}`}
              onClick={() => {
                handleTaps(item.id);
                handleSkillsNow(item.tap);
              }}
            >
              {item.tap}
            </li>
          );
        })}
      </ul>

      <div className="skills-content">
        {skillsNow.map((item) => {
          return (
            <div className="box" key={item.id}>
              {item.icon}
              <span className="same-title">{item.title}</span>
            </div>
          );
        })}
      </div>
    </div>
  );
}

export default Skills;
